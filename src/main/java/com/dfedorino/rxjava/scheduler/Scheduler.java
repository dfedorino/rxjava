package com.dfedorino.rxjava.scheduler;

/**
 * Абстракция для управления потоками выполнения задач.
 * Позволяет определить, в каком потоке будут выполняться задачи в реактивных приложениях.
 */
public interface Scheduler {

    /**
     * Планирует задачу для выполнения.
     *
     * @param task задача для выполнения
     * @throws RejectedExecutionException если задача не может быть запланирована
     *         (например, шедулер остановлен или нет доступных потоков)
     */
    void execute(Runnable task);

    /**
     * Останавливает шедулер и освобождает ресурсы.
     * После вызова новые задачи не принимаются.
     */
    void shutdown();

    /**
     * Проверяет, был ли шедулер остановлен.
     *
     * @return true если был вызван shutdown(), false в противном случае
     */
    boolean isShutdown();
}
