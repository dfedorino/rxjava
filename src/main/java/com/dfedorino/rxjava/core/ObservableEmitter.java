package com.dfedorino.rxjava.core;

/**
 * Интерфейс для эмиттера элементов Observable.
 * Предоставляет методы для отправки элементов, ошибок и уведомлений о завершении.
 *
 * @param <T> тип элементов, испускаемых этим эмиттером
 */
public interface ObservableEmitter<T> extends Disposable {

    /**
     * Отправляет следующий элемент потока.
     *
     * @param value следующий элемент
     */
    void onNext(T value);

    /**
     * Отправляет уведомление об ошибке.
     * После вызова этого метода дальнейшие уведомления не принимаются.
     *
     * @param error исключение, описывающее ошибку
     */
    void onError(Throwable error);

    /**
     * Отправляет уведомление о завершении потока.
     * После вызова этого метода дальнейшие уведомления не принимаются.
     */
    void onComplete();
}
