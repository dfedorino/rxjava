package com.dfedorino.rxjava.core;

/**
 * Интерфейс для получения уведомлений от Observable.
 * Observer подписывается на Observable и получает элементы потока,
 * уведомления об ошибках и завершении потока.
 *
 * @param <T> тип элементов, получаемых этим Observer
 */
public interface Observer<T> {
    
    /**
     * Вызывается при получении следующего элемента потока.
     *
     * @param item следующий элемент потока
     */
    void onNext(T item);
    
    /**
     * Вызывается при возникновении ошибки в Observable.
     * После вызова этого метода дальнейшие уведомления не поступают.
     *
     * @param t исключение, описывающее ошибку
     */
    void onError(Throwable t);
    
    /**
     * Вызывается при успешном завершении потока данных.
     * После вызова этого метода дальнейшие уведомления не поступают.
     */
    void onComplete();
}
